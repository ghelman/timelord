package net.chaosserver.timelord.swingui;

/**
 * Contains a bunch of constants for the various Swing Layouts.
 * They are defined here to avoid magic number style issues.
 */
public final class LayoutConstants {

    /** Private constructor. */
    private LayoutConstants() {}

    /** The padding for the larger inset. */
    public static final int BIG_INSET = 50;

    /** The padding for the smaller inset. */
    public static final int SMALL_INSET = 10;

    /** The heavier weight. */
    public static final int HEAVY_WEIGHT = 50;

    /** Child Frame X offset. */
    public static final int CHILD_FRAME_X_OFFSET = 10;

    /** Child Frame Y offset. */
    public static final int CHILD_FRAME_Y_OFFSET = 10;

    /** Small Text Area Height. */
    public static final int SMALL_TEXTAREA_HEIGHT = 5;

    /** Small Text Area Width. */
    public static final int SMALL_TEXTAREA_WIDTH = 20;

    /** Lightening Amount. */
    public static final int LIGHTEN_AMOUNT = 15;
}
